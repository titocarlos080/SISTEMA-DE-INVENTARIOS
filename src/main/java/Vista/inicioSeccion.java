      private void iniciarseccion() {
        String cod = txtUser.getText();
        conectorDB con = new conectorDB();
        try {
            PreparedStatement ps = con.concectar().prepareStatement("SELECT CODIGO,NOMBRE,DESCCRIPCION FROM VENDEDOR,PERSONA,CATEGORIAVENDEDOR WHERE IDCATEGORIAVENDEDOR=ID AND CI=CIPERSONA AND CODIGO=?");
            ps.setString(1, cod);
            ResultSet respuesta = ps.executeQuery();

            if (respuesta != null && respuesta.next()) {
                System.out.println(respuesta.getString("CODIGO"));
                if (cod.compareTo(respuesta.getString("CODIGO")) == 0) {

                    Index ini = new Index(true, respuesta.getString("NOMBRE") + " - " + respuesta.getString("DESCCRIPCION"));

                    this.dispose();

                    lblInfo.setText("");
                } else {
                    lblInfo.setText("Usuario incorrecto!");
                    lblInfo.setForeground(Color.red);
                }
            } else {
                lblInfo.setText("Usuario incorrecto!");
                lblInfo.setForeground(Color.red);
            }
            ps.close();

        } catch (SQLException ex) {
            System.out.println(ex);
        }

    }
